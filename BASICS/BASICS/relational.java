package BASICS;
public class relational {
    public static void main(String[] args) {
        int a = 10;
        float b =10.0f;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
