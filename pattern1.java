

public class pattern1 {
    public static void main(String[] args) {
        int n=5;
        int r=1;
        int s=n;
        while (r<=n){
            int i=1;
            while (i<=s){
                System.out.print(" * ");
                i++;
            }
            r++;
            System.out.println();
        }

    }
    
    
}
